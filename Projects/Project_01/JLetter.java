/**
 * A program that prints a block letter J composed of the characters J,A,V,A.
 *
 * @author Austin Hall
 * @version 1/20/17
 */
public class JLetter {
   /**
    * Prints large letter J one time.
    * @param args Command line arguments - (not used).
    */
   public static void main(String[] args) {
    
      //prints the letters J,A,V,A in a pattern to create one large letter J
      System.out.println("JAVAJAVAJAVA");
      System.out.println("JAVAJAVAJAVA");
      System.out.println("      JAVA  ");
      System.out.println("      JAVA  ");
      System.out.println("      JAVA  ");
      System.out.println("      JAVA  ");
      System.out.println("J     JAVA  ");
      System.out.println("JA    JAVA  ");
      System.out.println(" JAVAJAVA   ");
      System.out.println("  JAVAJA    ");
   }
}